-- Add comprehensive admin policies to investors table
CREATE POLICY "Admins can view all investors"
  ON public.investors FOR SELECT
  TO authenticated
  USING (
    EXISTS (
      SELECT 1 FROM public.user_roles
      WHERE user_id = auth.uid() AND role = 'admin'
    )
  );

